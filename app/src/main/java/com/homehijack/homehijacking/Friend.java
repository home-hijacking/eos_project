package com.homehijack.homehijacking;

public class Friend {
    public String name;
    public String address;
    public String email;
    public Boolean isHost;

    public Friend(String name, String address, String email, Boolean isHost){
        this.name = name;
        this.address = address;
        this.email = email;
        this.isHost = isHost;
    }
}
